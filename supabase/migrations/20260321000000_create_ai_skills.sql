CREATE TABLE IF NOT EXISTS ai_skills (
  skill_id    SERIAL PRIMARY KEY,
  name        TEXT NOT NULL,                          -- "Акти", "Склад", "Бухгалтерія"
  category    TEXT NOT NULL DEFAULT 'database',       -- database | format | rules | general
  keywords    TEXT[] NOT NULL DEFAULT '{}',           -- {"акт","наряд","заказ","відкри","закри"}
  prompt      TEXT NOT NULL,                          -- текст інструкцій для AI
  is_active   BOOLEAN NOT NULL DEFAULT true,
  priority    INTEGER NOT NULL DEFAULT 0,             -- вищій = важливіший (при конфлікті)
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Індекс для швидкого пошуку активних скілів
CREATE INDEX IF NOT EXISTS idx_ai_skills_active ON ai_skills (is_active) WHERE is_active = true;

-- RLS: повний доступ для авторизованих (дані не секретні, управляються через UI)
ALTER TABLE ai_skills ENABLE ROW LEVEL SECURITY;

CREATE POLICY "ai_skills_select" ON ai_skills FOR SELECT TO authenticated USING (true);
CREATE POLICY "ai_skills_insert" ON ai_skills FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "ai_skills_update" ON ai_skills FOR UPDATE TO authenticated USING (true) WITH CHECK (true);
CREATE POLICY "ai_skills_delete" ON ai_skills FOR DELETE TO authenticated USING (true);

